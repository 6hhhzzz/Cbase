    review_comment  TEXT,
    action_type     VARCHAR(16)  NOT NULL DEFAULT 'upload'
                            CHECK (action_type IN ('upload','update','delete')),
    pending_file_path VARCHAR(512),
    pending_metadata  JSONB       DEFAULT '{}'
