CREATE INDEX IF NOT EXISTS idx_da_document ON document_approvals(document_id);
CREATE INDEX IF NOT EXISTS idx_da_status ON document_approvals(status);

-- ============================================================
-- 对话与消息（修改自 v2）
-- 删除: scope_type, scope_id
-- 新增: kb_id（可选，对话不一定绑定 KB）
-- ============================================================

CREATE TABLE IF NOT EXISTS conversations (
    id              UUID         PRIMARY KEY,
    user_id         UUID         NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    title           VARCHAR(255),
    kb_id           UUID         REFERENCES knowledge_bases(id),
    space_id        UUID         REFERENCES spaces(id),
    status          VARCHAR(16)  NOT NULL DEFAULT 'active'
                            CHECK (status IN ('active', 'archived')),
    message_count   INTEGER      DEFAULT 0,
    created_at      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_conv_user_status ON conversations(user_id, status);
CREATE INDEX IF NOT EXISTS idx_conv_kb ON conversations(kb_id);
CREATE INDEX IF NOT EXISTS idx_conv_space ON conversations(space_id);
CREATE INDEX IF NOT EXISTS idx_conv_updated ON conversations(updated_at);

CREATE TABLE IF NOT EXISTS messages (
    id              BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    conversation_id UUID          NOT NULL REFERENCES conversations(id) ON DELETE CASCADE,
    role            VARCHAR(16)   NOT NULL CHECK (role IN ('user', 'assistant', 'system')),
    content         TEXT          NOT NULL,
    tokens_used     INTEGER,
    sources         JSONB,
    created_at      TIMESTAMP     DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_msg_conv_time ON messages(conversation_id, created_at);

-- ============================================================
-- 向量存储（修改自 v2）
-- 删除: owner_scope_type, owner_scope_id, classification,
--       visible_departments, security_level, chunk_access_level
-- 新增: kb_id
-- ============================================================

CREATE TABLE IF NOT EXISTS knowledge_chunks (
    id            VARCHAR(64)   PRIMARY KEY,
    doc_id        VARCHAR(64)   NOT NULL,
    chunk_index   INTEGER       NOT NULL,
    chunk_text    TEXT          NOT NULL,
    embedding     VECTOR(1024)  NOT NULL,
    kb_id         VARCHAR(64)   NOT NULL,
    status        VARCHAR(16)   NOT NULL DEFAULT 'active'
                          CHECK (status IN ('active', 'soft_deleted')),
    doc_effective_date DATE     NULL,
    doc_expiry_date  DATE       NULL,
    doc_version      VARCHAR(32) NULL,
    source_file   VARCHAR(512)  NOT NULL DEFAULT '',
    -- v5: 混合检索新增
    content_with_weight TEXT     NULL,         -- 关键词加权重复文本（BM25 增强）
    fts            tsvector      NULL,         -- PostgreSQL 全文搜索向量
    created_at    BIGINT        NOT NULL
);
CREATE INDEX IF NOT EXISTS idx_kc_doc_id ON knowledge_chunks(doc_id);
CREATE INDEX IF NOT EXISTS idx_kc_kb ON knowledge_chunks(kb_id);
CREATE INDEX IF NOT EXISTS idx_kc_status ON knowledge_chunks(status);
-- v5: HNSW 索引替代 IVFFlat，更高召回率
CREATE INDEX IF NOT EXISTS idx_kc_embedding_hnsw
    ON knowledge_chunks
    USING hnsw (embedding vector_cosine_ops)
    WITH (m = 16, ef_construction = 64);
-- v5: 全文搜索 GIN 索引
CREATE INDEX IF NOT EXISTS idx_kc_fts
    ON knowledge_chunks USING GIN (fts);

-- v5: 自动更新 fts 触发器
CREATE OR REPLACE FUNCTION update_kc_fts()
RETURNS TRIGGER AS $$
BEGIN
    NEW.fts = to_tsvector('simple',
        regexp_replace(COALESCE(NEW.content_with_weight, NEW.chunk_text), '([一-鿿])', ' \\1 ', 'g'));
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER IF NOT EXISTS trg_update_kc_fts
    BEFORE INSERT OR UPDATE OF chunk_text, content_with_weight
    ON knowledge_chunks
    FOR EACH ROW
    EXECUTE FUNCTION update_kc_fts();

