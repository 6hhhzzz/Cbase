-- ============================================================
-- 企业知识助手 — v4 ACE 架构初始化脚本
-- 权限模型: User Group → Space → ACE (Group ↔ KB ↔ Role)
-- 数据库: kes
-- ============================================================

-- ============================================================
-- pgvector 扩展
-- ============================================================
CREATE EXTENSION IF NOT EXISTS vector;

-- ============================================================
-- 认证鉴权（与 v2 基本一致，仅 users 去掉了 department）
-- ============================================================

CREATE TABLE IF NOT EXISTS users (
    id                UUID         PRIMARY KEY,
    username          VARCHAR(32)  NOT NULL UNIQUE,
    password          VARCHAR(255) NOT NULL,
    display_name      VARCHAR(64)  NOT NULL,
    is_global_admin   BOOLEAN      DEFAULT FALSE,
    created_at        TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    updated_at        TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_users_username ON users(username);

CREATE TABLE IF NOT EXISTS refresh_tokens (
    id          BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     UUID          NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    token       VARCHAR(512)  NOT NULL UNIQUE,
    expires_at  TIMESTAMP     NOT NULL,
    created_at  TIMESTAMP     DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_rt_token ON refresh_tokens(token);
CREATE INDEX IF NOT EXISTS idx_rt_user ON refresh_tokens(user_id);

-- ============================================================
-- v3: Space（顶层空间）
-- type_label 自由文本，不做枚举约束
-- ============================================================

CREATE TABLE IF NOT EXISTS spaces (
    id                 UUID         PRIMARY KEY,
    name               VARCHAR(128) NOT NULL,
    type_label         VARCHAR(64)  DEFAULT '',             -- 自由文本标签，如 "company"/"lab"/"community"
    description        TEXT         DEFAULT '',
    status             VARCHAR(16)  DEFAULT 'active' CHECK (status IN ('active', 'archived')),
    deleted_at         TIMESTAMP    NULL,
    last_accessed_at   TIMESTAMP,
    created_by         UUID         NOT NULL REFERENCES users(id),
    created_at         TIMESTAMP    NOT NULL DEFAULT NOW(),
    updated_at         TIMESTAMP    NOT NULL DEFAULT NOW()
);

-- ============================================================
-- v4: 全局用户组（可嵌套）+ 组成员
-- ============================================================

CREATE TABLE IF NOT EXISTS user_groups (
    id               UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    parent_group_id  UUID         REFERENCES user_groups(id) ON DELETE SET NULL,
    name             VARCHAR(128) NOT NULL,
    description      TEXT         DEFAULT '',
    is_system_admin  BOOLEAN      NOT NULL DEFAULT FALSE,
    created_by       UUID         NOT NULL REFERENCES users(id),
    created_at       TIMESTAMP    NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMP    NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_user_groups_parent ON user_groups(parent_group_id);

CREATE TABLE IF NOT EXISTS user_group_members (
    id         UUID       PRIMARY KEY DEFAULT gen_random_uuid(),
    group_id   UUID       NOT NULL REFERENCES user_groups(id) ON DELETE CASCADE,
    user_id    UUID       NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    joined_at  TIMESTAMP  NOT NULL DEFAULT NOW(),
    UNIQUE(group_id, user_id)
);
CREATE INDEX IF NOT EXISTS idx_ugm_user  ON user_group_members(user_id);
CREATE INDEX IF NOT EXISTS idx_ugm_group ON user_group_members(group_id);

-- ============================================================
-- v4: 角色定义（KB 级别权限套餐）
-- ============================================================

CREATE TABLE IF NOT EXISTS roles (
    id           UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    name         VARCHAR(64)  NOT NULL,
    description  TEXT         DEFAULT '',
    permissions  JSONB        NOT NULL DEFAULT '{}',
    is_system    BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at   TIMESTAMP    NOT NULL DEFAULT NOW()
);

