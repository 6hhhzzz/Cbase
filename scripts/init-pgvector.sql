-- ============================================================
-- v4: Space 管理员（关联具体 User）+ Space 准入组（关联用户组）
-- ============================================================

CREATE TABLE IF NOT EXISTS space_admins (
    space_id   UUID       NOT NULL REFERENCES spaces(id) ON DELETE CASCADE,
    user_id    UUID       NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    role       VARCHAR(20) NOT NULL CHECK (role IN ('owner', 'admin')),
    granted_by UUID       REFERENCES users(id),
    created_at TIMESTAMP  NOT NULL DEFAULT NOW(),
    PRIMARY KEY (space_id, user_id)
);

CREATE TABLE IF NOT EXISTS space_groups (
    id         UUID       PRIMARY KEY DEFAULT gen_random_uuid(),
    space_id   UUID       NOT NULL REFERENCES spaces(id) ON DELETE CASCADE,
    group_id   UUID       NOT NULL REFERENCES user_groups(id) ON DELETE CASCADE,
    joined_at  TIMESTAMP  NOT NULL DEFAULT NOW(),
    UNIQUE(space_id, group_id)
);
CREATE INDEX IF NOT EXISTS idx_sg_space ON space_groups(space_id);
CREATE INDEX IF NOT EXISTS idx_sg_group ON space_groups(group_id);

-- ============================================================
-- v4: Group 管理员（管"人"，与 Space 管理员解耦）
-- ============================================================

CREATE TABLE IF NOT EXISTS group_admins (
    group_id   UUID       NOT NULL REFERENCES user_groups(id) ON DELETE CASCADE,
    user_id    UUID       NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    role       VARCHAR(20) NOT NULL CHECK (role IN ('owner', 'admin')),
    granted_by UUID       REFERENCES users(id),
    created_at TIMESTAMP  NOT NULL DEFAULT NOW(),
    PRIMARY KEY (group_id, user_id)
);

-- ============================================================
-- v3: Knowledge Base（知识库）
-- visibility: space_wide（Space 内全员可见）| restricted（仅 kb_members 可见）
-- ============================================================

CREATE TABLE IF NOT EXISTS knowledge_bases (
    id          UUID         PRIMARY KEY,
    space_id    UUID         NOT NULL REFERENCES spaces(id) ON DELETE CASCADE,
    name        VARCHAR(128) NOT NULL,
    description TEXT         DEFAULT '',
    visibility  VARCHAR(16)  NOT NULL DEFAULT 'space_wide'
                        CHECK (visibility IN ('space_wide', 'restricted')),
    deleted_at  TIMESTAMP    NULL,
    created_by  UUID         NOT NULL REFERENCES users(id),
    created_at  TIMESTAMP    NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMP    NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_kb_space ON knowledge_bases(space_id);
CREATE INDEX IF NOT EXISTS idx_kb_visibility ON knowledge_bases(space_id, visibility);

-- ============================================================
-- v4: ACE 核心权限表（主体 → 资源 → 角色）
-- principal: group | user, resource: kb | document (Phase 3)
-- effect: allow | deny（deny 始终覆盖 allow）
-- ============================================================

CREATE TABLE IF NOT EXISTS access_control_entries (
    id              UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    space_id        UUID         NOT NULL REFERENCES spaces(id) ON DELETE CASCADE,
    resource_type   VARCHAR(16)  NOT NULL CHECK (resource_type IN ('kb', 'document')),
    resource_id     UUID         NOT NULL,
    principal_type  VARCHAR(16)  NOT NULL CHECK (principal_type IN ('group', 'user')),
    principal_id    UUID         NOT NULL,
    role_id         UUID         NOT NULL REFERENCES roles(id) ON DELETE CASCADE,
    effect          VARCHAR(8)   NOT NULL DEFAULT 'allow' CHECK (effect IN ('allow', 'deny')),
    priority        INT          NOT NULL DEFAULT 0,
    created_at      TIMESTAMP    NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMP    NOT NULL DEFAULT NOW(),
    UNIQUE(space_id, resource_type, resource_id, principal_type, principal_id)
);
CREATE INDEX IF NOT EXISTS idx_ace_space     ON access_control_entries(space_id);
CREATE INDEX IF NOT EXISTS idx_ace_resource  ON access_control_entries(resource_type, resource_id);
CREATE INDEX IF NOT EXISTS idx_ace_principal ON access_control_entries(principal_type, principal_id);

-- ============================================================
-- 文档管理（修改自 v2）
-- 删除: owner_scope_type, owner_scope_id, classification,
--       visible_departments, security_level, chunk_access_level
-- 新增: kb_id
-- ============================================================

CREATE TABLE IF NOT EXISTS document_meta (
    id               UUID         PRIMARY KEY,
    filename         VARCHAR(255) NOT NULL,
    file_type        VARCHAR(16)  NOT NULL,
    file_size        BIGINT       NOT NULL,
    file_path        VARCHAR(512) NOT NULL,
    space_id         UUID         NOT NULL REFERENCES spaces(id),  -- v4: 冗余 space_id 加速查询
    kb_id            UUID         NOT NULL REFERENCES knowledge_bases(id),
    status           VARCHAR(16)  NOT NULL DEFAULT 'active'
                             CHECK (status IN ('active', 'soft_deleted')),
    deleted_at       TIMESTAMP    NULL,
    expires_at       TIMESTAMP    NULL,
    doc_effective_date DATE       NOT NULL DEFAULT CURRENT_DATE,
    doc_expiry_date  DATE         NULL,
    doc_version      VARCHAR(32)  NULL,
    contributor_id   UUID         REFERENCES users(id),
    approval_status  VARCHAR(16)  NOT NULL DEFAULT 'approved'
                             CHECK (approval_status IN ('pending','approved','rejected')),
    tags             JSONB,
    ingest_status    VARCHAR(16)  NOT NULL DEFAULT 'pending'
                             CHECK (ingest_status IN ('pending','processing','completed','failed')),
    uploaded_by      UUID         NOT NULL REFERENCES users(id),
    created_at       TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    updated_at       TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_dm_kb ON document_meta(kb_id);
CREATE INDEX IF NOT EXISTS idx_dm_uploader ON document_meta(uploaded_by);
CREATE INDEX IF NOT EXISTS idx_dm_approval ON document_meta(approval_status);
CREATE INDEX IF NOT EXISTS idx_dm_ingest ON document_meta(ingest_status);
CREATE INDEX IF NOT EXISTS idx_dm_status ON document_meta(status);
CREATE INDEX IF NOT EXISTS idx_dm_expires ON document_meta(expires_at);
CREATE INDEX IF NOT EXISTS idx_dm_effective ON document_meta(doc_effective_date);
CREATE INDEX IF NOT EXISTS idx_dm_expiry ON document_meta(doc_expiry_date);
CREATE INDEX IF NOT EXISTS idx_dm_created ON document_meta(created_at);

-- ============================================================
